package day02datatypesmethodcreation;

public class Variables01 {

    public static void main(String[] args) {
        //ornek1: sehir ismi için bir variable olustur ve once Ankara sonrada izmir değerlerini atayip yazdır
        String cityName = "Ankara";
        System.out.println(cityName);
        cityName = "İzmir";
        System.out.println(cityName);


        cityName = "İstanbul";
        System.out.println(cityName);

        //ornek2: sehir plaka kodu icin bir once 34 sonra 35

        byte plaka = 34;
        System.out.println(plaka);
        plaka = 35;
        System.out.println(plaka);

        //ornek3:basarılı mı

        boolean basarıMı = true;

        System.out.println(basarıMı);
        basarıMı = false;
        System.out.println(basarıMı);






    }
}
